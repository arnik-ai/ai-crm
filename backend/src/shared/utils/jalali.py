"""تبدیل تاریخ میلادی به شمسی + محاسبه‌ی «ماه مالی» سفارشی.

ماه مالیِ کارفرما از یازدهمِ هر ماه شمسی شروع و در دهمِ ماه بعد تمام می‌شود؛
یعنی ۱۱ خرداد تا ۱۰ تیر = «ماهِ خرداد». این util بدون هیچ وابستگیِ بیرونی
(الگوریتم استانداردِ تبدیل) کار می‌کند تا استقرار ساده بماند.
"""
from datetime import date

_JALALI_MONTHS = [
    "فروردین", "اردیبهشت", "خرداد", "تیر", "مرداد", "شهریور",
    "مهر", "آبان", "آذر", "دی", "بهمن", "اسفند",
]

_G_DAYS_IN_MONTH = [0, 31, 59, 90, 120, 151, 181, 212, 243, 273, 304, 334]


def gregorian_to_jalali(gy: int, gm: int, gd: int) -> tuple[int, int, int]:
    """(سال، ماه، روز) میلادی → (سال، ماه، روز) شمسی. الگوریتم استاندارد."""
    if gy > 1600:
        jy = 979
        gy -= 1600
    else:
        jy = 0
        gy -= 621
    gy2 = gy + 1 if gm > 2 else gy
    days = (
        365 * gy + (gy2 + 3) // 4 - (gy2 + 99) // 100 + (gy2 + 399) // 400
        - 80 + gd + _G_DAYS_IN_MONTH[gm - 1]
    )
    jy += 33 * (days // 12053)
    days %= 12053
    jy += 4 * (days // 1461)
    days %= 1461
    if days > 365:
        jy += (days - 1) // 365
        days = (days - 1) % 365
    if days < 186:
        jm = 1 + days // 31
        jd = 1 + days % 31
    else:
        jm = 7 + (days - 186) // 30
        jd = 1 + (days - 186) % 30
    return jy, jm, jd


def fiscal_month(d: date) -> tuple[str, str]:
    """ماهِ مالیِ یک تاریخ را برمی‌گرداند: (کلیدِ مرتب‌سازی، برچسبِ خوانا).

    قانون: اگر روزِ شمسی ≥ ۱۱ باشد، همان ماهِ شمسی؛ وگرنه ماهِ شمسیِ قبل.
    مثال: ۳ تیر ۱۴۰۵ → ماهِ خرداد ۱۴۰۵ (کلید "1405-03").
    """
    jy, jm, jd = gregorian_to_jalali(d.year, d.month, d.day)
    if jd < 11:
        # به ماهِ قبل تعلق دارد
        jm -= 1
        if jm == 0:
            jm = 12
            jy -= 1
    key = f"{jy}-{jm:02d}"
    label = f"{_JALALI_MONTHS[jm - 1]} {jy}"
    return key, label
